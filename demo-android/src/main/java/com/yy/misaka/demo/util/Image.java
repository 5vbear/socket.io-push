package com.yy.misaka.demo.util;

import android.widget.ImageView;
import com.nostra13.universalimageloader.core.DisplayImageOptions;
import com.nostra13.universalimageloader.core.ImageLoader;
import com.yy.misaka.demo.R;

/**
 * User: lxl
 * Date: 3/11/15
 * Time: 5:00 PM
 */
public class Image {

    private static final DisplayImageOptions PORTRAIT = new DisplayImageOptions.Builder().showImageForEmptyUri(R.drawable.misaka).showImageOnLoading(R.drawable.misaka).cacheInMemory(true).cacheOnDisk(true).build();

    public static void displayPortrait(String uri, ImageView imageView) {
        ImageLoader.getInstance().displayImage(uri, imageView, PORTRAIT);

    }
}
